# Orijinal veriyi kopyala
df_processed = df.copy()

# Hedef değişkeni ayır
y = df_processed['grade_category']

# Gereksiz sütunları çıkar (G1, G2, G3 - çünkü bunlar final notunu direkt belirliyor)
